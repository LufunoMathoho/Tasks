let x = 8;
let y = 12;

if (x > y) {
    console.log("x is greater than y");
} else {
    console.log("x is not greater than y");
}

if (x <= y) {
    console.log("x is less than y");
} else {
    console.log("x is equals to y");
}

if (x == y) {
    console.log("x is equal to y");
} else {
    console.log("x is not equal to y");
}

if (x != y) {
    console.log("x is not equal to y");
} else {
    console.log("x is equal to y");
}
